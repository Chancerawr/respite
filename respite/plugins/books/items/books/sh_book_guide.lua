<h1>The Survival Guide to the Respite.</h1>
<h3>By Johnny Walker</h3>
		<p align="center"><font color='blue' size='6'>1. What is the Respite?</font></p><br/>
<font color='black' size='3'>The Respite(s), are the spaces in time that we, the Drifters, Plastics, Shadow, Mannequins and everything else inhabits. It is believed that the Respites are created by Drifters, and the constant collapsing of these respites are due to their owners not being there anymore.

		<br/><br/>
The Respite(s) also is the home of 'the Mist' (will be talked about later), and our presensce in the Respites causes over time degradation of the Respite itself. After roughly a week or more of being on a Respite, they usually collapse, but first forms a portal so all beings may escape their demise.
		<br/><br/>
Most of the drifters have formed new lives within the Respite in an effort to mimic their old as best as possible. These may include having a new sexual partner, building new things, caring for a car, or trying to get property and homes. This, in term, makes it highly recommended that the reader of this guide does the same in order to maintain their sanity.
		<br/><br/>
The Author also recommends not attempting suicide.
		<br/><br/>
		</font>

		<p align="center"><font color='blue' size='6'>2. The Mist</font></p><br/>
<font color='black' size='3'>The Mist is a phenomenon that is carried out throughout all of the Respites, much like weather like snowing, raining and clouds. The Mist, however, comes for a certain amount of reasons, and there have been a total of three confirmed mists, albeit the author suggests there's four.
		<br/><br/>
1. Red Mist - This mist comes directly as a result of a Drifter attacking another Drifter. This Mist causes hallucination to anyone caught in it, and is very unpleasant.
		<br/><br/>
2. Pink Mist - The reason for this one to exist is unknown. It causes Sensory Deprivation and may cause blindness, deafness or loss of feelings for as long as you stay in it.
		<br/><br/>
3. Black Mist - This mist always summons the Shadow People. It is also believed that if you stay in the black mist for too long, you may end up killing yourself.
		<br/><br/>
4. White Mist - Cover for the Monsters of the Respite to ambush you.
		<br/><br/>
5. Blue Mist - This mist will destroyed technology, is attracted to water, and causes LSD-like Hallucinations if you are in it.
		<br/><br/>
Whenever a Mist is happening, it's highly recommended you stay indoors. If it's a black mist, stay in a warehouse with a Shard weapon.
		<br/><br/>
		</font>
			
		<p align="center"><font color='blue' size='6'>3. The Plastic</font></p><br/>
<font color='black' size='3'>The Plastics are the product of Drifters, who created them first from plastic, and after that the plastics have continued to make themselves. They are unable to bleed due to this, but also are not able to communicate to non-plastics. The Plastics have their own written language.
		<br/><br/>
The Plastics are considered to be friendly to Drifters, albeit scared, at first glance, unless the plastic have experienced drifters before, or have a personality that aren't phased by new things. They have their own set of ethics and appear to be fully conciouss beings.
		<br/><br/>
Plastics have shown to be kind and gratious hosts to drifters, and very rarely are provocative and aggressive towards them, unless the Drifters are arguing and being aggressive towards them.
		<br/><br/>
It's recommended that if you see a plastic, you give it some distance to study you from. Violent cases of Plastics are rare, but don't take the risk.
		<br/><br/>
		</font>
			
		<p align="center"><font color='red' size='6'>4. The Mannequins</font></p><br/>
<font color='black' size='3'>The Mannequins are, just as their name suggests, Mannequins. Albeit at one point they may have been used to display clothes and brand marks on them, they are now sentient beings that are known to be highly violent towards drifters.
		<br/><br/>
There have been multiple cases in which lone-drifters have been killed by Mannequins, and so far the known ways to destroy a Mannequin is to throw it out of the Respite, burn them, or blow them up. 
		<br/><br/>
Once you have lost eyesight of a Mannequin, they will follow the shortest path to you as fast as possible, and attempt to position themselves behind you, where they lunge at and kill you.
		<br/><br/>
It's recommended that if you believe you are followed by a Mannequin to get another Drifter to come down and help you, or a group of them. The Author also recommends that if you are only two people handling one, that you tell each other when you are blinking, as they also move during blinking.
		<br/><br/>
		</font>
			
		<p align="center"><font color='red' size='6'>5. The Shadow People</font></p><br/>
<font color='black' size='3'>The Shadow People are, just as their name suggests once more, Shadows. They are capable of being killed if they are a weak shadow, albeit stronger ones require sharded weaponry.
		<br/><br/>
The Shadow people can go through walls, and will simply go directly for their pray in order to kill them as swift as possible. As thus, it is recommended to avoid confined spaces that would allow them to flank you or block you in a room. Fighting them should be done by someone with a Shard weapon, and inside a Warehouse.
		<br/><br/>
The Shadow people have, along with the Mannequins, have shown no real 'friendliness', and will attack any drifters and plastics on sight. They may however only appear in Black mists, and is thought of to be their 'Calling Card' to attack.
		<br/><br/>
As such, the Author recommends staying away from Mannequins and shadow people. A telltale sign of when there are Shadow people around is due to the voices and whispering in your head. As thus, you are not necessarily insane.
		<br/><br/>
		</font>
	
