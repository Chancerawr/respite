		<p align="center"><font color='blue' size='6'>7. Survival Tips</font></p><br/>
<font color='black' size='3'>When it comes to tips, there are five essential items you will have want to secured for yourself by the time atleast a week has passed by:
		<br/><br/>
1. Friends. You need someone you can call a friend and someone who you can call a best friend. They will rely on you, you will rely on them, and you both are comfortable in each other's presensce. Don't just give away a bunch of things to strangers, but instead befriend them, talk to them. The more friends you have, the better off you are.
		<br/><br/>
2. Guns. In the Respite, there's a shit ton of monsters meaning to kill you, gut you, or drive you insane. The best way to keep them at bay is to have some form of a gun. Keep in mind, if you are going for a shotgun, be mindful of where you point it. It shoots pellets, not slugs.
		<br/><br/>
3. Communication. Be sure you can always communicate with everyone else. Use the Frequency 199.6 and get yourself a radio, fast. A lack of communication can lead to you missing key events, or even die to something the rest knew about.
		<br/><br/>
4. Light. You need a light, the Respite is a dark and gloomy place. The Light will help you see where you're going and keep your morale up. Just know when to turn it off, in case you see too much.
		<br/><br/>
5. House. Make sure you have a property you feel safe at in each Respite, or else it will just be a waste of time.
		<br/><br/>
With almost everything covered, I have one important note to leave you on.
		<br/><br/>
Once you have discovered it all, gotten all equipment and been here for a long time. Then you must remember one key thing: There are still new people every day in the Respite. Help them, talk to them, be kind to them. If you do that, you might have secured yourself a friend for life.
		<br/><br/>
With that said, I thank you for reading the Survival Guide, and hope that you will find your stay in the Respite comfortable and as close to normal life as possible.
