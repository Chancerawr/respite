A portal is defined as any surface or material that transports one object to another location. Most portals generally do not lead to other respites, but there are plenty that do. Usually once a portal is established, its exit will not change unless it closes, but this can depend upon how it was created. A portal created by a living being may have the ability to change the destination while the portal is open.
