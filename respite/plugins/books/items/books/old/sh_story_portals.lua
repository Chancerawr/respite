Enhanced chips are vital for most technological uses of portals, and can easily be applied to most objects in order to give them portal-like properties. Ammunition is a common, yet expensive implementation of this fact. Portal enhanced weaponry becomes incredibly good at penetrating through armor. This is not because it is higher velocity, or more powerful, but because it teleports the material it makes physical contact with somewhere else. That being the case, portal bullets lose some power, instead of blowing things apart, they leave bullet sized holes. Portal enhanced melee weapons also work in a similar manner. A portal blade will teleport what it cuts somewhere else, making it seemingly very sharp. However, portal enhanced weapons, with enough use, will lose effectiveness, and eventually return back to normal. 
