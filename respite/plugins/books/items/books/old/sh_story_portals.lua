Physically, most portals appear to have a colored outline, and a blackness everywhere else. They come in a variety of shapes, from doors, gates, holes, cracks, etc. The  color of a portal may also vary depending on the nature of its connection. Most consider red portals and black portals to be dangerous, while white portals are somewhat neutral. It is wise to avoid red portals, especially jagged, crack-like ones, as they are typically an indicator of an abomination threat.
	<br/><br/>
Many devices in the Void make use of portals in how they function. The requisition device is a common portal device, as it actually opens portals in the sky above to provide requested supplies. Other more simple devices may have a small portal within them open at all times, which can be used to transfer input and output objects around. Devices such as a chip pouch, or an enhanced backpack make use of portals to create extra space.
